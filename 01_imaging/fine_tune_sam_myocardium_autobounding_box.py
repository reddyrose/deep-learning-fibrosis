def predict(image):

  # AUTOBOUNDING BOX LOGIC:

  # # 1) use uNet to predict rough mask
  rough_image = preprocess_grayscale(image)

  estimated_mask = get_estimated_mask(rough_image, unet_model)

  plt.imshow(np.array(estimated_mask))
  plt.show()

  # 2) create a bounding box out of the rough mask
  prompt = get_bounding_box(estimated_mask)

  # 3) replace "global_prompt" with the custom bounding box
  inputs = processor(images=image, input_boxes=[[prompt]], return_tensors="pt").to(device)

  with torch.no_grad():
      outputs = model(**inputs, multimask_output=False)

  pred_seg_prob = torch.sigmoid(outputs.pred_masks.squeeze(1))

  pred_seg_prob = pred_seg_prob.cpu().numpy().squeeze()
  pred_seg = (pred_seg_prob > 0.5).astype(np.uint8)

  return pred_seg

def dice_coefficient(prediction, ground_truth):
    intersection = np.logical_and(prediction, ground_truth)
    intersection = np.count_nonzero(intersection)

    union = np.count_nonzero(prediction) + np.count_nonzero(ground_truth)

    dice = (2 * intersection) / (union)  # Add a small value to avoid division by zero
    return dice

def get_mean_T1(image, ROI):
  image = np.array(image)
  image = np.dot(image[...,:3], [0.299, 0.587, 0.114])

  ROI = np.array(ROI)

  mean_T1_ROI = ROI * image
  print(np.max(mean_T1_ROI))

  mean_T1_ROI = mean_T1_ROI * (4000/255)

  mean_T1_value = np.sum(mean_T1_ROI)/np.sum(ROI)

  return mean_T1_ROI, mean_T1_value

def calculate_statistics(values):
  ROI_T1_values = values[values != 0]

  std_dev_t1 = np.std(ROI_T1_values)
  percentile_99 = np.percentile(ROI_T1_values, 99)
  percentile_75 = np.percentile(ROI_T1_values, 75)
  percentile_50 = np.percentile(ROI_T1_values, 50)  # Median

  return std_dev_t1, percentile_99, percentile_75, percentile_50

