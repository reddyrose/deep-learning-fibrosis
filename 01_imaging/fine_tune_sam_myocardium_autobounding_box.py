def remove_blobs(image, min_blob_size):

  # Find contours in the binary image
  contours, _ = cv2.findContours(image, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

  # Filter contours based on blob size and remove them
  filtered_contours = [cnt for cnt in contours if cv2.contourArea(cnt) < min_blob_size]

  # Create a mask of the filtered contours
  mask = np.zeros_like(image)
  cv2.drawContours(mask, filtered_contours, -1, 255, thickness=cv2.FILLED)

  # Invert the mask
  mask = 255 - mask

  # Apply the mask to the original image to remove the blobs
  result = cv2.bitwise_and(image, image, mask=mask)

  return result

def post_processing(prediction):

  # Define the structuring element and apply erosion
  kernel = np.ones((3, 3), np.uint8)

  eroded_mask = cv2.erode(prediction, kernel, iterations=1)
  blurred_mask = cv2.GaussianBlur(eroded_mask, (3, 3), 0)

  blurred_mask = (blurred_mask > 0.95).astype(np.uint8)
  blurred_mask = remove_blobs(blurred_mask, 120)

  # Update binary_preds with the transformed mask
  processed_pred = blurred_mask

  return processed_pred

def get_estimated_mask(input_img, unet_model):

    predicted_mask = unet_model.predict(np.expand_dims(input_img, axis=0))
    predicted_mask = post_processing(predicted_mask[0])

    return predicted_mask


