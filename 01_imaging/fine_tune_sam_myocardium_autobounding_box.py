def rgb2gray(rgb):
    gray = np.dot(rgb[...,:3], [0.299, 0.587, 0.114])
    gray = np.expand_dims(gray, axis=2)
    return gray

def convert_to_grayscale(sample):
    sample_array = np.array(sample["image"])

    # Convert the image to grayscale using the rgb2gray function
    gray_array = rgb2gray(sample_array)

    #Rescale array to dicom range
    dicom_range = 4000
    png_range = 255

    scaled_gray_array = gray_array * (dicom_range/ png_range)

    sample["image"] = np.array(scaled_gray_array)
